  String translate(String key) {
    return _localizedStrings[locale.languageCode]?[key] ??
           _localizedStrings['en']?[key] ??
           key;
  }

  static String languageName(String code) => _languageNames[code] ?? code;
  static String languageFlag(String code) => _languageFlags[code] ?? '🌐';

  static Locale localeFromCode(String code) {
    final parts = code.split('-');
    if (parts.length == 2) return Locale(parts[0], parts[1]);
    return Locale(code);
  }
}

class AppLocalizationsDelegate extends LocalizationsDelegate<AppLocalizations> {
  const AppLocalizationsDelegate();

  @override
  bool isSupported(Locale locale) => AppLocalizations.supportedLanguages.contains(locale.languageCode);

  @override
  Future<AppLocalizations> load(Locale locale) async => AppLocalizations(locale);

  @override
  bool shouldReload(AppLocalizationsDelegate old) => false;
}
